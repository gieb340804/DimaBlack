def sort_even_odd(arr):
  """
  Сортирует целочисленный массив так, что все четные числа идут перед нечетными.
  """

  # Разделяем массив на четные и нечетные элементы.
  even = []
  odd = []
  for num in arr:
    if num % 2 == 0:
      even.append(num)
    else:
      odd.append(num)

  # Сортируем четный массив.
  even.sort()

  # Сортируем нечетный массив.
  odd.sort()

  # Объединяем четный и нечетный массивы.
  return even + odd